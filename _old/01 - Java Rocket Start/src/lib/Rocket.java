package lib;

public class Rocket {

	private double rotation;
	private int gasOutletLevel;

	public double getRotation() {
		return rotation;
	}

	public void setRotation(double rotation) {
		this.rotation = rotation;
	}

	public int getGasOutletLevel() {
		return gasOutletLevel;
	}

	public void setGasOutletLevel(int gasOutletLevel) {
		this.gasOutletLevel = gasOutletLevel;
	}

}
